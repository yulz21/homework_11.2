def capitalize():

    '''Функция, которая делает заглавными все буквы каждого слова в строке,
     поступившей на вход функции'''

    cap_string = str.upper(input())
    return cap_string

def title_format():

    '''Функция, которая делает заглавными первые буквы каждого слова в строке,
     поступившей на вход функции'''

    title_string = str.title(input())
    return title_string
